#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int	i;
	int n;

	i = 0;
	if (s2[0] == '\0')
		return ((char *)s1);
	while (s1[i] != '\0')
	{
		n = 0;
		while (s1[i + n] == s2[n] && (s1[i + n] != '\0'))
		{
			if (s2[n + 1] == '\0')
			{
				s1 = s1 + i;
				return ((char *)s1);
			}
			n++;
		}
		i++;
	}
	return (NULL);
}
