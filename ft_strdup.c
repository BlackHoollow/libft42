#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		n;
	char	*str;

	i = 0;
	n = ft_strlen(s1);
	if (!(str = (char *)malloc(sizeof(char) * (n + 1))))
		return (NULL);
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
